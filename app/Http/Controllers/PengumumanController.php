    {

        if (socialitations::create([
            "title" => $request->title,
            "description" => $request->description,
        ])) {
            return redirect()->intended('/pengumumman');
            $request->session()->flash('success', "Pengumuman berhasil dibuat");
        }

        $request->session()->flash('error', "Password atau email anda salah!");

        return back();
    }

